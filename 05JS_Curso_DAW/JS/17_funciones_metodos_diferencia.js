// Diferencia entre metodos y funciones
const numero1 = 20;
const numero2 = "20";

// parseInt() es una funcion para convertir de string a entero
console.log(parseInt(numero2)); // parseInt();
// toString() es una funcion para convertir de entero a string 
console.log(numero1.toString()); // .toString(10);

/* La diferencia entre metodos y funciones es que metodos son procedimientos y funciones son un conjunto de instrucciones que realizan una accion
procedimientos: son metodos que realizan una accion especificada, para que un metodo sea una funcion 
se debe usar la palabra reservada function y debe tomar una entrada y retornar una salida
funciones: son un conjunto de instrucciones que realizan una tarea o accion especificada */