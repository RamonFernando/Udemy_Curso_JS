// 7. setTimeout

// Retrasa la ejecución de una función.
// Ejecutar un mensaje después de 2 segundos
setTimeout(() => {
    console.log("Han pasado 2 segundos");
}, 2000); // 2 segundos

// La función pasada a setTimeout se ejecutará después del tiempo especificado (en milisegundos).
// Es útil para retrasar la ejecución de una acción o para programar una tarea 
// que debe ocurrir después de un cierto intervalo.