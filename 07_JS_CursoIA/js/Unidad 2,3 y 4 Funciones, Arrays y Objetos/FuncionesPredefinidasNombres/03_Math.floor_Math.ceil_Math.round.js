// 3. Math.floor, Math.ceil, y Math.round

// Redondean números hacia abajo, arriba, o al valor más cercano.
let numero = 4.7;

// Redondear hacia abajo
console.log(Math.floor(numero)); // 4

// Redondear hacia arriba
console.log(Math.ceil(numero)); // 5

// Redondear al número más cercano (De 5-9 se redondea hacia arriba y de 0-4 hacia abajo)
console.log(Math.round(numero)); // 5


