// Crear un array de frutas
let frutas = ["manzana", "naranja", "plátano", "uva"];

// Verificar si el array incluye "naranja"
let incluyeNaranja = frutas.includes("naranja");
console.log(incluyeNaranja); // true

// Verificar si el array incluye "fresa"
let incluyeFresa = frutas.includes("fresa");
console.log(incluyeFresa); // false

// includes devuelve true si el elemento está en el array y false si no está.