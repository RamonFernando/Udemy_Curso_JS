// Crear un array de palabras
let palabras = ["Hola", "mundo", "JavaScript"];

// Unir los elementos con espacios
let frase = palabras.join(" ");
console.log(frase); // "Hola mundo JavaScript"

// Unir los elementos con guiones
let fraseGuiones = palabras.join("-");
console.log(fraseGuiones); // "Hola-mundo-JavaScript"

