public class ArrayBidimencionalesEjemplo1 {

	/**
	 * Ejemplo Array Bidimencional
	 */
	public static void main(String[] args) {
		// Mostrando un array del ejercicio 13 de 4 filas y 3 columnas. 
		
		// Tablas o matriz de 4 filas y 3 columnas. 
		double medidas[][] = new double[4][3];
		
		// Fila 0 columna 1,2,3
		medidas[0][0] = 8.5;
		medidas[0][1] = 1020;
		medidas[0][2] = 40;
		
		// Fila 1 columna 1,2,3
		medidas[1][0] = 14.2;
		medidas[1][1] = 1021;
		medidas[1][2] = 35;
		
		// Fila 2 columna 1,2,3
		medidas[2][0] = 11;
		medidas[2][1] = 1022;
		medidas[2][2] = 45;
		
		// Fila 3 columna 1,2,3
		medidas[3][0] = 7;
		medidas[3][1] = 1023;
		medidas[3][2] = 50;
	}

}
