public class Practica12Constantes {

	/**
	 * Variables Constantes
	 */
	public static void main(String[] args) {
		// Las variables constantes siempre van con mayuscula y con separacion
		// de palabras con guiones bajos.
		// Se suele representar con la palabra final para declarar una variable
		// constante o final.
		// Una variable constante o final es aquella que nunca cambia de valor,
		// osea que su valor es siempre el mismo.

		final float PI = 3.141592F;
		float radio = 10F;
		float areaCirculo = PI * radio * radio;

		System.out.println(areaCirculo);

	}

}
