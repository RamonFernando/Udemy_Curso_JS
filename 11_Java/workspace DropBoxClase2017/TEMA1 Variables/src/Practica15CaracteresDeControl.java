public class Practica15CaracteresDeControl {

	/**
	 * Caracteres de Control
	 */
	public static void main(String[] args) {

		// Salto de linea \n
		System.out.println("Tu nombre es \nCarlos ");

		// Tabulaciones \t
		System.out.println("120\t130\t140\t150\t160");
		System.out.println("170\t180\t190\t155\t165");

		// Comillas Dobles \"Palabra que queremos escribir \"
		System.out
				.println("Su nota en el examen podria considerarse \"mejorable\" ");

		// Comillas Simples \'Frase\'
		System.out
				.println("Su nombre en el examen podria considerarse \'mejorable\'");

		// Contrabarra \\ Si quisieramos poner 2 contrabarras tendriamos que
		// poner 4 Ejemplo \\\\
		System.out.println("La ruta de la carpeta usuario es C:\\User");

	}

}
