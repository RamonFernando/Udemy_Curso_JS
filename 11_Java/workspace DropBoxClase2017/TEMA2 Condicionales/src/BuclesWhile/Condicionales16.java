public class Condicionales16 {

	/**
	 * PDF-Ejercicio de Bucles 16/22
	 * Descripcion:
	 * Bucles: Variable contador y acumulador.
	 */
	public static void main(String[] args) {
		// Modificar el ejercicio 15 para que muestre los 100 primeros numeros
		// impares.

		int i = 1;
		while (i <= 100) {
			System.out.println(2 * i - 1);
			i++;
		}
		System.out.println("Fin");
	}

}
