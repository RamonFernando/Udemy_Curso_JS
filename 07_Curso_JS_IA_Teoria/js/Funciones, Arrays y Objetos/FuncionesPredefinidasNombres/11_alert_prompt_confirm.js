
// Mostrar una alerta
alert("¡Hola! Bienvenido a la página");

// Pedir un valor al usuario
let nombre = prompt("¿Cuál es tu nombre?");
console.log(`Hola, ${nombre}`);

// Confirmar una acción
let confirmado = confirm("¿Estás seguro?");
console.log(confirmado); // true (si acepta) o false (si cancela)

// Estas funciones son interactivas y solo funcionan en navegadores.

/*
EXPLICACION DE LOS MÉTODOS
alert(): Muestra una ventana de alerta con un mensaje.
prompt(): Muestra una ventana de entrada de texto con un mensaje y un campo de entrada.
confirm(): Muestra una ventana de confirmación con un mensaje y botones para aceptar o cancelar.
*/